function formatValue(
  value: string,
  replacements?: Record<string, string | number>,
): string {
  if (!replacements) return value;
  return value.replace(/{{(\w+)}}/g, (_, key) =>
    replacements[key] != null ? String(replacements[key]) : "",
  );
}

export function translate(
  language: Language,
  key: string,
  replacements?: Record<string, string | number>,
): string {
  const segments = key.split(".");
  let current: TranslationValue | undefined = translations[language];
  for (const segment of segments) {
    if (current && typeof current === "object") {
      current = (current as TranslationRecord)[segment];
    } else {
      current = undefined;
      break;
    }
  }
  if (typeof current === "string" || typeof current === "number") {
    return formatValue(String(current), replacements);
  }
  // fallback to English
  if (language !== "en") {
    return translate("en", key, replacements);
  }
  return key;
}

export function detectLanguageFromText(text: string): Language | null {
  if (!text) return null;
  const hanMatches = text.match(/[\p{Script=Han}]/gu)?.length ?? 0;
  const latinMatches = text.match(/[A-Za-z]/g)?.length ?? 0;
  if (hanMatches === 0 && latinMatches === 0) return null;
  if (hanMatches > 0 && hanMatches >= latinMatches) return "zh";
  if (latinMatches > 0 && latinMatches >= hanMatches) return "en";
  return null;
}

export const DEFAULT_LANGUAGE: Language = "zh";

export function normalizeLanguage(value?: string | null): Language {
  return value === "zh" ? "zh" : "en";
}
